# Example 2
def calculate(a, b):  # 'a' এবং 'b' হল প্যারামিটার (যেগুলো ফাংশনে পাঠানোর জন্য প্লেসহোল্ডার)
    d = a + b  # 'a' এবং 'b' এর যোগফল হিসাব করা
    print(d)  # 'd' একটি লোকাল ভ্যারিয়েবল যা হিসাব করা ফলাফল ধারণ করে এবং সেটি প্রিন্ট করা হয়

# ইউজারের ইনপুট গ্রহণ করা
sum1 = int(input("Enter the first number: "))  # 'sum1' হল আর্গুমেন্ট, প্রথম সংখ্যার জন্য ইউজার ইনপুট
sum2 = int(input("Enter the second number: "))  # 'sum2' হল আর্গুমেন্ট, দ্বিতীয় সংখ্যার জন্য ইউজার ইনপুট

# ফাংশন কল করা ইউজারের প্রদত্ত মান দিয়ে
calculate(sum1, sum2)  # 'sum1' এবং 'sum2' আর্গুমেন্ট হিসেবে ফাংশনে পাঠানো হয়েছে

