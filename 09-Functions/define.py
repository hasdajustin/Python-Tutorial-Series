# Example 3
def totalmarks(english, maths, bangla):  # 'english', 'maths', এবং 'bangla' প্যারামিটার যা প্রতিটি বিষয়ের নম্বরের প্রতিনিধিত্ব করে
    total = english + maths + bangla  # মোট নম্বর হিসাব করা, সব বিষয়ের যোগফল
    average = total / 3  # গড় নম্বর হিসাব করা, মোট নম্বর ৩ দিয়ে ভাগ করা
    print(f"Total Marks: {total}")  # মোট নম্বর প্রিন্ট করা
    print(f"Average Marks: {average}")  # গড় নম্বর প্রিন্ট করা

# প্রতিটি বিষয়ের নম্বরের জন্য ইউজার ইনপুট গ্রহণ করা
english = int(input("Enter the marks for English: "))  # 'english' হল আর্গুমেন্ট, ইংরেজির নম্বরের জন্য ইউজার ইনপুট
maths = int(input("Enter the marks for Maths: "))  # 'maths' হল আর্গুমেন্ট, গাণিতিক নম্বরের জন্য ইউজার ইনপুট
bangla = int(input("Enter the marks for Bangla: "))  # 'bangla' হল আর্গুমেন্ট, বাংলা নম্বরের জন্য ইউজার ইনপুট

# ফাংশন কল করা ইউজারের প্রদত্ত মান দিয়ে
totalmarks(english, maths, bangla)  # 'english', 'maths', এবং 'bangla' আর্গুমেন্ট হিসেবে ফাংশনে পাঠানো হয়েছে
