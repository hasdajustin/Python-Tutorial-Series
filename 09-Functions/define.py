# Example 1
def sum(a, b):  # 'a' এবং 'b' হল প্যারামিটার, যেগুলো ফাংশনে মান পাঠানোর জন্য ব্যবহৃত হবে
    c = a + b  # 'a' এবং 'b' এর যোগফল হিসাব করা
    print(c)  # যোগফল প্রিন্ট করা

sum(1, 2)  # '1' এবং '2' হল আর্গুমেন্ট, যা ফাংশন কল করার সময় পাস করা হয়েছে

